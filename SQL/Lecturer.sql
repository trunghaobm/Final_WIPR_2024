-- HÀM INSERT
CREATE OR ALTER PROCEDURE INSERT_LECTURER 
	@FACTORY	NVARCHAR(MAX),
    @EMAIL		NVARCHAR(MAX),
	@FIRSTNAME	NVARCHAR(MAX),
	@LASTNAME	NVARCHAR(MAX),
	@BIRTHDAY	NVARCHAR(MAX),
	@GENDER		NVARCHAR(MAX),
	@PHONE		NVARCHAR(MAX),
	@ADDRESS	NVARCHAR(MAX),
	@AVATAR		NVARCHAR(MAX),
    @PASSWORD	NVARCHAR(MAX),
    @ACTIVE		BIT
AS
BEGIN
    IF DBO.CHECK_INSERT_LECTURER(@EMAIL) = 1
    BEGIN
        DECLARE @ID NVARCHAR(MAX);
        SET @ID = CONCAT('LEC', FORMAT(NEXT VALUE FOR LECTURER_SEQ, '00000000'));
        INSERT INTO LECTURER (
				ID
				,FACTORY 
				,EMAIL 
				,FIRSTNAME
				,LASTNAME
				,BIRTHDAY
				,GENDER
				,PHONE
				,[ADDRESS] 
				,AVATAR 
				,[PASSWORD]
				,ACTIVE )
        VALUES (
				@ID
				,@FACTORY 
				,@EMAIL 
				,@FIRSTNAME
				,@LASTNAME
				,@BIRTHDAY
				,@GENDER
				,@PHONE
				,@ADDRESS 
				,@AVATAR 
				,CONVERT(NVARCHAR(MAX), HASHBYTES('SHA2_512',@PASSWORD), 2)
				,@ACTIVE )
		SELECT 1 AS RESULT;
    END
    ELSE
    BEGIN
        THROW 50001, 'EMAIL OR MSSV ALREADY EXISTS', 1;
    END
END;
GO

-- HÀM UPDATE
CREATE OR ALTER PROCEDURE UPDATE_LECTURER 
    @ID			NVARCHAR(MAX),
	@FACTORY	NVARCHAR(MAX),
    @EMAIL		NVARCHAR(MAX),
	@FIRSTNAME	NVARCHAR(MAX),
	@LASTNAME	NVARCHAR(MAX),
	@BIRTHDAY	NVARCHAR(MAX),
	@GENDER		NVARCHAR(MAX),
	@PHONE		NVARCHAR(MAX),
	@ADDRESS	NVARCHAR(MAX),
	@AVATAR		NVARCHAR(MAX),
    @PASSWORD	NVARCHAR(MAX),
    @ACTIVE		BIT
AS
BEGIN
    IF DBO.CHECK_UPDATE_LECTURER(@ID) = 1
    BEGIN
        UPDATE LECTURER
        SET 
			FACTORY		= @FACTORY,
			EMAIL		= @EMAIL,
			FIRSTNAME	= @FIRSTNAME,
			LASTNAME	= @LASTNAME,
			BIRTHDAY	= @BIRTHDAY,
			GENDER		= @GENDER,
			PHONE		= @PHONE,
			[ADDRESS]	= @ADDRESS,
			AVATAR		= @AVATAR,
            [PASSWORD]	= CONVERT(NVARCHAR(MAX), HASHBYTES('SHA2_512',@PASSWORD), 2),
            ACTIVE = @ACTIVE
        WHERE ID = @ID;
    END
    ELSE
    BEGIN
        THROW 50002, 'EMAIL ALREADY EXISTS FOR ANOTHER USER', 1;
    END;
END;
GO

-- HÀM DELETE
CREATE OR ALTER PROCEDURE DELETE_LECTURER 
    @ID NVARCHAR(MAX)
AS
BEGIN
    IF DBO.CHECK_DELETE_LECTURER() = 1
    BEGIN
        DELETE FROM LECTURER WHERE ID = @ID;
    END
    ELSE
    BEGIN
        THROW 50003, 'DELETE OPERATION NOT ALLOWED', 1;
    END
END;
GO

CREATE OR ALTER PROCEDURE TRYLOGIN_LECTURER
    @EMAIL NVARCHAR(MAX),
    @PASSWORD NVARCHAR(MAX)
AS
BEGIN
    SET NOCOUNT ON;

	DECLARE @MYPASS NVARCHAR(MAX);

    SELECT @MYPASS = [PASSWORD]
    FROM LECTURER
    WHERE EMAIL = @EMAIL;

	IF CONVERT(NVARCHAR(MAX), HASHBYTES('SHA2_512', @PASSWORD), 2) = @MYPASS
	BEGIN
		SELECT 1 AS Result;
	END
	ELSE
	BEGIN
		THROW 50005, N'MẬT KHẨU KHÔNG ĐÚNG', 1;
	END
END;
GO


