-- TẠO BẢNG
DROP TABLE IF EXISTS LECTURER;
GO

CREATE TABLE LECTURER (
    ID			NVARCHAR(MAX),
	FACTORY		NVARCHAR(MAX),
	FIRSTNAME	NVARCHAR(MAX),
	LASTNAME	NVARCHAR(MAX),
	BIRTHDAY	NVARCHAR(MAX),
	GENDER		NVARCHAR(MAX),
    EMAIL		NVARCHAR(MAX),
	PHONE		NVARCHAR(MAX),
	[ADDRESS]	NVARCHAR(MAX),
	AVATAR		NVARCHAR(MAX),
    [PASSWORD]	NVARCHAR(MAX),
    ACTIVE BIT
);
GO

-- TẠO SEQUENCE
DROP SEQUENCE IF EXISTS LECTURER_SEQ;
GO
CREATE SEQUENCE LECTURER_SEQ
    START WITH 1
    INCREMENT BY 1
    MINVALUE 1
    NO MAXVALUE
    CACHE 10;
GO

-- HÀM TẠO BẢNG TỪ CHUỖI
CREATE OR ALTER FUNCTION GETTABLE_LECTURER (@EMAIL NVARCHAR(MAX))
RETURNS TABLE
AS
RETURN
(
    SELECT * 
    FROM LECTURER LEC 
    WHERE LEC.EMAIL = @EMAIL
);
GO



-- HÀM KIỂM TRA INSERT
CREATE OR ALTER FUNCTION CHECK_INSERT_LECTURER(@EMAIL NVARCHAR(MAX))
RETURNS BIT
AS
BEGIN
    DECLARE @RESULT BIT = 0;
    IF NOT EXISTS (SELECT 1 FROM LECTURER WHERE EMAIL = @EMAIL)
        SET @RESULT = 1;
    RETURN @RESULT;
END;
GO

